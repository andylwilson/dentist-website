    <p>Shopping carts (and the stores associated with them) are one of the most vital aspects of making money with websites, and are essential to any company that intends for their website to offer products or services for sale. I’ve found that there is an entire category of software focused entirely on providing shopping cart and storefront solutions, and several companies provide popular end-to-end options such as Shopify, 3dcart, Magento, as well as solutions from the big website-builder companies like SquareSpace and Wix.</p>

    <p>Creating your own sounds like the way to go for most businesses however (good for us web developers!), as LifeHacker’s Melanie Pinola explains:</p>

    <p class="quote">Hosting your own e-commerce site gives you more control over the shop and its presentation, is better for business branding, makes moving to another web host easier, and can be more cost-effective. You can, for example, avoid transaction and listing fees, but you'll still have to pay processing fees from your payment gateway.</p>

    <p>There are open source shopping cart solutions as well, such as osCommerce and Zen Cart, and Melanie continues:</p>

    <p class="quote">Free shopping cart scripts like osCommerce and Zen Cart are the critical backbones of many online stores. Most web hosts will offer the open source osCommerce script as a quick install, but there are a great many others you can compare on Wikipedia and use instead [...]</p>

    <p>I also found that the shopping cart and storefront services offered by companies like SquareSpace and Wix, while easier to implement for the average user, are monetized via the software-as-a-service model, which charges the user fees per use (such as a transaction or per-item basis), which can be very cumbersome for anyone intending to create a serious long-term business. FinancesOnline’s shopping cart software article notes:</p>

    <p class="quote">These cloud-based, SaaS, shopping cart software solutions are quicker to install and maintain. Thus they are very attractive to businesses that have limited inventory as well as site needs. However, the costs can easily rise to a point where a company ends up paying several times more than they would for a product that is licensed.
    </p>

